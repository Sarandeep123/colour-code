c=input("Enter the colour code:")
if c=='Y':
    print(c,"-->Yellow")
elif c=='R':
    print(c,"-->Red")
elif c=='G':
    print(c,"-->Green")
elif c=='B':
    print(c,"-->Blue")
elif c=='V':
    print(c,"-->Voilet")
elif c=='O':
    print(c,"-->Orange")
elif c=='I':
    print(c,"-->Indigo")
else:
    print("Invalid colour code")
